class Classifier(NeuralComponent):
    """Neural network classifier"""
    
    def __init__(self, name: str, input_dim: int, output_dim: int, **kwargs):
        super().__init__(name, input_dim, output_dim, **kwargs)
        # Simulate weights
        self._weights = np.random.randn(output_dim, input_dim) * 0.01
    
    def forward(self, x: np.ndarray) -> np.ndarray:
        x = np.asarray(x).flatten()
        logits = self._weights @ x
        # Softmax
        exp_logits = np.exp(logits - np.max(logits))
        return exp_logits / np.sum(exp_logits)
    
    def predict(self, x: np.ndarray) -> int:
        probs = self.forward(x)
        return int(np.argmax(probs))


